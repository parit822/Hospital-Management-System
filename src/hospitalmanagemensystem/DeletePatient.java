    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String patientid=pid.getText();
        int Pid = Integer.parseInt(patientid);
        try{
            DefaultTableModel model =(DefaultTableModel)Table.getModel();
            //Load Driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            // Create Connection
            Connection con=DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/hospital","root","PRADEEP@123");
            // Create Statement
            String query="SELECT * FROM patient WHERE id=?";
            PreparedStatement pstmt=con.prepareStatement(query);
            pstmt.setInt(1,Pid);
           ResultSet rs= pstmt.executeQuery();
            while (rs.next()) {                
                int pid=rs.getInt("id");
                String patid=Integer.toString(pid);
                String pname=rs.getString("name");
                int age=rs.getInt("age");
                String Age=Integer.toString(age);
                String dname=rs.getString("doctorName");
                
                String row[]={patid,pname,Age,dname};
                
                model.addRow(row);
            }
            con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

